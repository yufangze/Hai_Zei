#include <stdio.h>

int main() {
    int f[3][1005] = {0};
    f[1][0] = f[1][1] = 1;
    f[2][0] = f[2][1] = 1;
    int n = 2;
    while (f[n % 3][0] < 1000) {
        n++;
        for (int i = 1; i <= f[(n + 2) % 3][0]; i++) {
            f[n % 3][i] = f[(n + 1) % 3][i] + f[(n + 2) % 3][i];
        }
        f[n % 3][0] = f[(n + 2) % 3][0];
        for (int j = 1; j <= f[n % 3][0]; j++) {
            if (f[n % 3][j] < 10) continue;
            f[n % 3][j + 1] += f[n % 3][j] / 10;
            f[n % 3][j] %= 10;
            f[n % 3][0] += (f[n % 3][0] == j);
        }
    }
    printf("%d\n", n);
    return 0;
}
